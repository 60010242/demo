package com.example.demo;

public class Productjson {
	private int Sstock;
	private int Mstock;
	private int Lstock;
	private int XLstock;
	public int getSstock() {
		return Sstock;
	}
	public void setSstock(int sstock) {
		Sstock = sstock;
	}
	public int getMstock() {
		return Mstock;
	}
	public void setMstock(int mstock) {
		Mstock = mstock;
	}
	public int getLstock() {
		return Lstock;
	}
	public void setLstock(int lstock) {
		Lstock = lstock;
	}
	public int getXLstock() {
		return XLstock;
	}
	public void setXLstock(int xLstock) {
		XLstock = xLstock;
	}
	
	
}
